package com.jshnd.virp.exception;

import com.jshnd.virp.exception.VirpException;

public class VirpAnnotationException extends VirpException {

	public VirpAnnotationException() {
	}

	public VirpAnnotationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public VirpAnnotationException(String arg0) {
		super(arg0);
	}

	public VirpAnnotationException(Throwable arg0) {
		super(arg0);
	}
}
