package com.jshnd.casrom;

public class CasromException extends RuntimeException {

	public CasromException() {
		super();
	}

	public CasromException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CasromException(String arg0) {
		super(arg0);
	}

	public CasromException(Throwable arg0) {
		super(arg0);
	}

}
